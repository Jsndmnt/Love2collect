// Construire aussi l'index EN->FR pour l'affichage
const POKEMON_EN_TO_FR = {};
Object.entries(POKEMON_FR_TO_EN).forEach(([fr, en]) => {
  if (!POKEMON_EN_TO_FR[en]) POKEMON_EN_TO_FR[en] = fr.charAt(0).toUpperCase() + fr.slice(1);
});

export function translateToEN(frName) {
  if (!frName) return frName;
  const lower = frName.toLowerCase().trim();
  // Correspondance exacte
  if (POKEMON_FR_TO_EN[lower]) return POKEMON_FR_TO_EN[lower];
  // Recherche partielle
  for (const [fr, en] of Object.entries(POKEMON_FR_TO_EN)) {
    if (lower.includes(fr) || fr.includes(lower)) return en;
  }
  // Pas trouvé → retourner tel quel (nom anglais ou inconnu)
  return frName;
}

export function getFrenchName(enName) {
  if (!enName) return enName;
  const lower = enName.toLowerCase().trim();
  return POKEMON_EN_TO_FR[lower] || enName;
}

